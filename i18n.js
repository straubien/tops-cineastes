var TC_LANG = 'fr';

function t(key) {
  var lang = TC_I18N[TC_LANG];
  return (lang && lang[key] !== undefined) ? lang[key] : (TC_I18N.fr[key] || key);
}

function tf(key, vars) {
  var s = t(key);
  if (vars) Object.keys(vars).forEach(function(k) {
    s = s.split('{' + k + '}').join(String(vars[k]));
  });
  return s;
}

function tpl(key, n) {
  return tf(key, { n: n, s: n !== 1 ? 's' : '' });
}

function applyLang() {
  document.documentElement.lang = TC_LANG;
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
  var btn = document.getElementById('nav-lang') || document.getElementById('sp-lang');
  if (btn) btn.textContent = TC_LANG === 'fr' ? 'EN' : 'FR';
}

function toggleLang() {
  TC_LANG = TC_LANG === 'fr' ? 'en' : 'fr';
  applyLang();
  if (typeof onLangChange === 'function') onLangChange();
}
